<h1 align="center">	About the Project</h1>
<p align="center">&nbsp;</p>
<p>The <a href="http://www.nyu.edu/projects/sanger/project/index.php">Margaret Sanger Papers Project</a> (MSPP) is an historical editing project sponsored by the New York University’s <a href="http://library.nyu.edu/">Division of Libraries</a>. Formed by Dr. Esther Katz in 1987 to locate, arrange, edit, research, and publish the papers of the noted birth control pioneer, it was, until 2014, sponsored by  NYU’s <a href="http://history.as.nyu.edu/page/projects">Department of History</a>. For more on the Project see <a href="https://www.nyu.edu/projects/sanger/project/index.php">Margaret Sanger Papers Project</a>.</p>
<p>&nbsp;</p>
<h2 align="center">Publications</h2>
<h3><u>Microfilm</u></h3>
<p>The MSPP has published a two-series <a href="https://www.nyu.edu/projects/sanger/publications/microfilm.php">microfilm edition</a>: 
<p><a href="https://www.nyu.edu/projects/sanger/publications/smith_series.php"><em>The Smith College Collections</em></a> -- Work on the <em>Smith College Collections</em> entailed the rearrangement and organization of over 50,000 Sanger documents in the Margaret Sanger collection and seventeen other collections at the Sophia Smith Collection and Smith College Archives.
<p><a href="https://www.nyu.edu/projects/sanger/publications/collected_documents.php"><em>The Collected Documents Series</em></a> -- Work on the <em>Collected Documents Series</em> included a ten-year international search of over 1,500 archives and private collections, photocopying material and organizing over 9,000 documents for publication.<p>Both series have been published with a printed reel guide that includes an item-level index by University Publications of America, a division of Lexis-Nexis (now <a href="http://cisupa.proquest.com/ws_display.asp?filter=upa_leaf&item_id={045AB6D6-ABC6-439D-ACFB-F7A37718D427}">Pro-Quest</a>).
<h3><u>Book</u></h3>
<p>The MSPP is completing the final volume of its four-volume book edition, <a href="file:///C|/Users/Esther%20Katz/Downloads/Book.php"><em>The Selected Papers of Margaret Sanger</em></a>, published by the <a href="http://www.press.uillinois.edu/books/catalog/74xsd6pe9780252074608.html">University of Illinois Press</a>.</p>
