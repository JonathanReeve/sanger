        <h1 align="center">About the Edition</h1>
<h2>The Collection</h2>
<body>
<p><em>The Speeches and Articles of Margaret Sanger (1911-1959)</em>, is a web-based edition of Sanger’s  writings, including articles, pamphlets, speeches, stories, and interviews, 
produced by <a href="http://www.nyu.edu/projects/sanger/index.php"> The Margaret Sanger Papers Project (MSPP). This collection of papers, speeches, interviews, articles and other writings by birth control reformer Margaret Sanger (1879-1966), is drawn from materials
collected by the staff of the <a href="http://www.nyu.edu/projects/sanger/index.php"> Margaret Sanger Papers Project (MSPP) in the course of preparing a 2-series microfilm, <em>The <a href="https://www.nyu.edu/projects/sanger/publications/microfilm.php">Margaret 
Sanger Papers Project Microfilm:</a> <em>Smith College Collection</em> and <em>Collected Documents</em> published in 1996 and 1997), the Library of Congress microfilm of Sanger’s papers, and 
the MSPP’s four-volume book edition, <a href="http://www.nyu.edu/projects/sanger/publications/book.php"><em>The Selected Papers of Margaret Sanger</em></a> published by Illinois University Press.</p>
<p>Unlike much of the historical material currently available on the Internet, this edition is a completely vetted, historically accurate digital version of her writings and speeches that conform to established standards, 
both in terms of technical features of its encoding, and providing accurate renderings of the texts. It  provides open access to complete versions of those articles, speeches and statements, most frequently cited and those that are often inaccessible to general 
readers.  In this digital edition, the Project seeks to make these documents much more accessible to researchers.</p>
<p>In a collection where there is so much material on a handful of subjects, readers may search documents by date ranges, publication source, and 
type of document (speech, draft article, etc.), as well as by simple word.  While such searches often return too many matches, they are invaluable for a researcher who wants to see each instance of Sanger’s use of a word or term.  A reader could search for the word "abortion" and go through each of them to get 
a sense of her views.  However, in such a search will pick up not just discussions of abortion, but mere use of the word.  In many instances Sanger simply said that birth control would reduce the frequency of 
abortions.  To solve the problem of getting too many, often not relevant “hits,” the Project has prepared a subject index. In the above example, this index will help generate fewer, but more relevant results.</p>
<p>After eliminating multiple versions , the editors have included nearly 1,000 documents, each verified by date and title. We have searched for published versions when we only have drafts, and have culled from multiple versions to create the most 
complete documents. Each document was then carefully  transcribed , proofread, and encoded each document, employing the XML P-5 encoding system developed for the humanities by the TEI (Text Encoding Initiative). Documents 
were then indexed by subject.</p>

<h2>Transcription</h2>

<p>We have transcribed the documents staying as close to the original copy text as possible, while still making the documents easily readable.  
Changes, amendations, and corrections to the document made by Sanger have been retained , as has most punctuation. Slips of the pen, spelling errors, and other elements 
that would impede word searches have been silently corrected.  Any text added to the document by the editors (supplied dates, our reading of illegible or partially obscured words) is rendered in [square brackets and italics]. 
Source notes at the top of each transcription provides information regarding the location or original publication information. The location on our microfilm edition is cited as 
follows: LCM (Library of Congress microfilm), MSM (“S” for Smith College Collections and “C”Collected Documents), followed by the reel and frame numbers.</p>
<p>Please note: These transcriptions have been mounted for research purposes only and the Sanger Project requests that Sanger Project URL be used when referring to any of these materials. 
Links may be made to our site (though we wish to be notified), but under no circumstances are the transcriptions to be copied and mounted onto another server without express permission.</p>
<h2>Encoding</h2>
<p>We are using a subset of XML created for Humanities text, called <a href="http://www.tei-c.org/index.xml">(TEI)">TEI</a>; within that we are using a subset created documentary electronic editions, called <a href="http://wyatt.elasticbeanstalk.com/mep/editors.html">MEP</a>, the Standardized General Markup Language <a href="https://en.wikipedia.org/wiki/XML">(XML)</a>. We used <a href="http://www.oxygenxml.com/doc/versions/17.1/ug-editor/index.html#introduction.html#introduction">Oxygen</a> as our tagging software. 
