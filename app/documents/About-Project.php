<h1 align="center">	About the Project</h1>
<p align="center"><a href="http://www.nyu.edu/projects/sanger/project/index.php">Margaret Sanger Papers Project</a> (MSPP) is an historical editing project sponsored by the New York University’s <a href="http://library.nyu.edu/">Division of Libraries</a>. 
Formed by Dr. Esther Katz in 1987 to locate, arrange, edit, research, and publish the papers of the noted birth control pioneer, it was until 2014, sponsored by  NYU’s <a href="http://history.as.nyu.edu/page/projects">Department of History</a>. For more on the Project see <a href="https://www.nyu.edu/projects/sanger/project/index.php">Margaret Sanger Papers Project</a></p>
<p>&nbsp;</p>
<h2 align="center">Publications</h2>
<h3><u>Microfilm </u></h3>
<p>The Margaret Sanger Papers Project has produced a two-series microfilm edition, <a href="http://cisupa.proquest.com/ksc_assets/catalog/2681_2679_margaretsangerpapers%5B2%5D.pdf"><em>The Margaret Sanger Papers Microfilm:</em></a>
 <a href="https://www.nyu.edu/projects/sanger/publications/microfilm.php"><em>Smith College Collections</em></a> and the <a href="https://www.nyu.edu/projects/sanger/publications/collected_documents.php"><em>Collected Documents Series</em></a> published by University Publications of America (now part of Proquest). Work on the <em>Smith College Collections</em> entailed the rearrangement and organization of over 50,000 Sanger documents in the Margaret Sanger collection and seventeen other collections at the
 Sophia Smith Collection and Smith College Archives.  The <em>Collected Documents Series</em>
 included a ten-year international search of over 1,500 archives and private collections, photocopying material and organizing over 9,000 documents for publication. Both series have been published with a printed reel guide 
For a draft index created by the Project for the Library of Congress, see <a href="https://www.nyu.edu/projects/sanger/publications/library_of_congress.php">https://www.nyu.edu/projects/sanger/publications/library_of_congress.php</a>.</p>
<p>&nbsp;</p>

<h3><u>Book  Edition</u></h3>
<p>The Project is currently completed  on the final volume of its four-volume book edition, The Selected Papers of Margaret Sanger, published by the <a href="http://www.press.uillinois.edu/" \t "_new">University of Illinois Press</a>. For the first three volumes, see:
<ol><a href="https://www.nyu.edu/projects/sanger/publications/volume_i.php"><em>Volume 1: The Woman Rebel, 1900-1928</em></a></ol>
<ol><a href="https://www.nyu.edu/projects/sanger/publications/volume_ii.php"><em>Volume 2: Birth Control Comes of Age</em></a></ol>
<ol><a href="https://www.nyu.edu/projects/sanger/publications/volume_iii.php"><em>Volume 3: The Politics of Planned Parenthood</em></a></ol>
<p>Volume 4: <a href="https://www.nyu.edu/projects/sanger/publications/volume_iv.php"><em>’Round the World  for Birth Contro</em></a> will be published in the winter of 2016.</p>
<p>&nbsp;</p>
<h3><u>Image Edition</u></h3>
<p>The Project also published a digital edition of a small sample of documents related to Sanger's publication of The Woman Rebel. See Model Editions Partnership: <a href="https://www.nyu.edu/projects/sanger/documents/mswomanrebel.php"><em>Margaret Sanger and "The Woman Rebel"</em></a>.</p>
